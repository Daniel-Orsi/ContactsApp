package com.example.contacts2.main

class Contact(
    name: String,
    surname: String,
    job: String,
    adress: String,
    tel: String,
    email: String
) {


    var name = ""
    var surname = ""
    var job = ""
    var adress = ""
    var tel = ""
    var email = ""


    init {
        this.name = name
        this.surname = surname
        this.job = job
        this.adress = adress
        this.tel = tel
        this.email = email
    }


}